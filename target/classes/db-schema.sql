create table if not exists USERS(
ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
EMAIL VARCHAR(255) NOT NULL,
PASSWORD VARCHAR(255),
FNAME VARCHAR(255) DEFAULT 'Flashcard User',
LNAME VARCHAR(255),
CONSTRAINT emailConstraint UNIQUE (EMAIL));
--syntax verified on local
create table if not exists GROUPS(
ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
GROUPNAME VARCHAR(255) NOT NULL,
DESCRIPTION VARCHAR(255),
CREATIONDATE TIMESTAMP DEFAULT 0,
UPDATEDATE TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
CONSTRAINT nameConstraint UNIQUE (GROUPNAME));
--syntax verified on local
create table if not exists USERGROUP(
USERID INTEGER,
GROUPID INTEGER,
FOREIGN KEY(USERID) REFERENCES USERS(ID),
FOREIGN KEY(GROUPID) REFERENCES GROUPS(ID));
--syntax verified on local
create table if not exists CARD(
ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
QUESTION VARCHAR(255),
QUESTIONCODE VARCHAR(255),
ANSWER VARCHAR(255),
ANSWERCODE VARCHAR(255));
--syntax verified on local
create table if not exists GROUPCARD(
GROUPID INTEGER,
CARDID INTEGER,
FOREIGN KEY(GROUPID) REFERENCES GROUPS(ID),
FOREIGN KEY(CARDID) REFERENCES CARD(ID));
